  echo
  if ask_yes_no "Configure environment settings (.env)?"; then
    do_env_config
  fi

  # Optional: PostgreSQL setup
  echo
  if ask_yes_no "Set up PostgreSQL for ADS-B history tracking?"; then
    do_postgres_setup
  fi

  # Health check
  echo
  info "Running health check..."
  do_health_check
}

