# Convert profile menu selections to bitmask
selections_to_mask() {
  local selections="$1"
  local mask=0
  for sel in $selections; do
    case "$sel" in
      1) mask=$((mask | PROFILE_CORE)) ;;
      2) mask=$((mask | PROFILE_MARITIME)) ;;
      3) mask=$((mask | PROFILE_WEATHER)) ;;
      4) mask=$((mask | PROFILE_SECURITY)) ;;
      5) mask=$PROFILE_FULL ;;
      6) mask=-1 ;; # custom
    esac
  done
  echo $mask
}

